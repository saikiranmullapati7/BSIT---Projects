// 15.	Declare a variable but do not assign any value to it. Print the variable and print its data type using typeof.

let number = 511;

let string = "SAI";

let boolean = true;

let myUndefined;

let Null = null;

let bigint = 9007199254740991n;

let symbol = Symbol("id");

console.log("Number type      :", typeof number);
console.log("String type      :", typeof string);
console.log("Boolean type     :", typeof boolean);
console.log("Undefined type   :", typeof myUndefined);
console.log("null type        :", typeof Null);
console.log("bigInt type      :", typeof bigint);
console.log("symbol of        :", typeof symbol);